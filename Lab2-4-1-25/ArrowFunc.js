//normal Function
let hello = "";

hello = function() {
  return "Hello World!";
}
console.log(hello());

//Arrow Function
let hello1 = "";

hello1 = () => {
  return "Hello World!";
}
console.log(hello1());

//Arrow function with parameters
let hello2="";
hello2=(val)=>"Hello "+val;
console.log(hello2("Venu!"));
